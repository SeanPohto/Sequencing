public class ForLoops
{
    public void run() {
        for (int i = 0; i <= 15; i++) { 
            System.out.println("step 1 " + i);
        }
        for (int j = 10; j >= 0; j -= 2) {
            System.out.println("step 2 " + j);
        }
        for (int k = 10; k >= 0; k -= 3) {
            System.out.println("step 3 "+ k);
        }
    }
}